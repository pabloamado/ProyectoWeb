package response;

public class GenericResponse {

	private String msg;
	private boolean success;
	
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public boolean isSuccess() {
		return success;
	}
	public void setSuccess(boolean sended) {
		this.success = sended;
	}
	
	}
